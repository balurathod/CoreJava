package com.rathod.oops;

public class MathExample {
public static void main(String[] args) {
	System.out.println(Math.abs(-123.12));
	System.out.println(Math.E);
	System.out.println(Math.ceil(13.4));
	System.out.println(Math.ceil(13.9));
	System.out.println(Math.round(13.5));
	System.out.println(Math.round(13.9));
	System.out.println(Math.floor(13.5));
	System.out.println(Math.floor(13.7));
}
}
